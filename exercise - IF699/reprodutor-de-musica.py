#se você digitasse uma palavra-chave em relação à música, você selecionaria ela

keyword = input()
if keyword == 'amor':
  print(f'Voce selecionou a musica: SE FOR AMOR - Joao Gomes e Vitor')
elif keyword == 'ficha':
  print(f'Voce selecionou a musica: FICHA LIMPA - Gusttavo Lima')
elif keyword == 'chega':
  print(f'Voce selecionou a musica: CHEGA E SENTA - John')
elif keyword == 'arranhao':
  print(f'Voce selecionou a musica: ARRANHAO - Henrique e Juliano')
else:
  print(f'Voce selecionou a musica: VOLTA COMIGO BB - Ze Vaqueiro')